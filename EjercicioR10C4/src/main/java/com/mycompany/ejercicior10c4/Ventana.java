    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nombres=jTextField1.getText();
    String errora = "";
    String errorb = "";
    String errorc = "";
    int numeroInscripcion = 0;
    double patrimonio = 0;
    int estrato = 0;
    try{
    numeroInscripcion=Integer.parseInt(jTextField2.getText());
    }catch(NumberFormatException A){
        errora = "El valor del numero de inscripcion ingresado es incorrecto";
    }
    try{
    patrimonio=Double.parseDouble(jTextField3.getText()); 
    }catch(NumberFormatException B){
        errorb = "El valor del patrimonio ingresado es incorrecto";
    }
    try{
    estrato=Integer.parseInt(jTextField4.getText());
    }catch(NumberFormatException C){
        errora = "El valor del estrato ingresado es incorrecto";
    } 
    if (!errora.isEmpty()) {
        JOptionPane.showMessageDialog(rootPane, errora, "Error", JOptionPane.ERROR_MESSAGE);
        jButton2ActionPerformed(null);}
    if (!errorb.isEmpty()) {
        JOptionPane.showMessageDialog(rootPane, errorb, "Error", JOptionPane.ERROR_MESSAGE);
        jButton2ActionPerformed(null);}
    if (!errorc.isEmpty()) {
        JOptionPane.showMessageDialog(rootPane, errorc, "Error", JOptionPane.ERROR_MESSAGE);
        jButton2ActionPerformed(null);}    
    Matricula matricula = new Matricula(nombres,numeroInscripcion,patrimonio,estrato);
    
    jTextField5.setText(matricula.getNombres());
    jTextField6.setText(Integer.toString(matricula.getNInscripcion())); 
    jTextField7.setText(Double.toString(matricula.getMatricula())); 
    
    }//GEN-LAST:event_jButton1ActionPerformed

