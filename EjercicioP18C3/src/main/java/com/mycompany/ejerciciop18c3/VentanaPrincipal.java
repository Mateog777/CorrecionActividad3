    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String errora = "";
        String errorb = "";
        String errorc = "";
        String errord = "";
        String nombre = jTextField1.getText();
        int codigo = 0;
        double horas_trabajadas_mes = 0;
        double valor_hora = 0;
        double porcentaje_retencion = 0;
        try {
            codigo = Integer.parseInt(jTextField2.getText());
        } catch (NumberFormatException a) {
            errora = "El codigo ingresado no se puede convertir a entero";
        }
        try {
            horas_trabajadas_mes = Double.parseDouble(jTextField3.getText());
        } catch (NumberFormatException b) {
            errorb = "El numero de horas trabajadas es incorrecto";
        }
        try {
            valor_hora = Double.parseDouble(jTextField4.getText());
        } catch (NumberFormatException c) {
            errorc = "El valor de la hora es incorrecto";
        }
        try {
            porcentaje_retencion = Double.parseDouble(jTextField5.getText());
        } catch (NumberFormatException d) {
            errord = "El porcentaje de retencion es incorrecto, ingreselo en formato 0.*";
        }
        Empleado empleado = new Empleado(horas_trabajadas_mes, valor_hora, porcentaje_retencion);
        jLabel8.setText("El nombre es: " + nombre);
        jLabel9.setText("El codigo es: " + Integer.toString(codigo));
        jLabel6.setText(empleado.calcularSalarioBruto());
        jLabel7.setText(empleado.calcularSalarioNeto());
        if (!errora.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, errora, "Error", JOptionPane.ERROR_MESSAGE);
            jButton2ActionPerformed(null);
        }
        if (!errorb.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, errorb, "Error", JOptionPane.ERROR_MESSAGE);
            jButton2ActionPerformed(null);
        }
        if (!errorc.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, errorc, "Error", JOptionPane.ERROR_MESSAGE);
            jButton2ActionPerformed(null);
        }
        if (!errord.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, errord, "Error", JOptionPane.ERROR_MESSAGE);
            jButton2ActionPerformed(null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

