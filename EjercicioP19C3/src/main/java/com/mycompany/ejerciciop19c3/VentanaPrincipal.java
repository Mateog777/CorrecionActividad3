    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int lado = 0;
        String error = "";
        try {
            lado = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException a) {
            error = "El valor de lado ingresado es incorrecto";
        }

        Triangulo triangulo1 = new Triangulo(lado);
        jTextField2.setText(Double.toString(triangulo1.calculoAltura()));
        jTextField3.setText(Double.toString(triangulo1.calcularPerimetro()));
        jTextField4.setText(Double.toString(triangulo1.calcularArea()));
        if (!error.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, error, "Error", JOptionPane.ERROR_MESSAGE);
            jButton2ActionPerformed(null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

