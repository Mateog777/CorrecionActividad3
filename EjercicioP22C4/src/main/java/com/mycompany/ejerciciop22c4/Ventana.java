    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String errora = "";
        String errorb = "";
        int horasTrabajadas = 0;
        double salarioPorHora = 0;
        try {
            salarioPorHora = Double.parseDouble(jTextField2.getText());
        } catch  (NumberFormatException a){
            errora = "El valor del salario por hora es incorrecto";
        }
        try {
            horasTrabajadas = Integer.parseInt(jTextField3.getText());
        }catch (NumberFormatException b){
              errorb = "El valor de las horas trabajadas es incorrecto";
        }
    Empleado empleado = new Empleado(nombre, salarioPorHora, horasTrabajadas);
    jTextField4.setText (empleado.getNombre());
    jTextField5.setText (empleado.getSalario());
    if (!errora.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, errora, "Error", JOptionPane.ERROR_MESSAGE);
            jButton2ActionPerformed(null);
        }
    if (!errorb.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, errorb, "Error", JOptionPane.ERROR_MESSAGE);
            jButton2ActionPerformed(null);
        }    

    }//GEN-LAST:event_jButton1ActionPerformed

